#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*new;
	int		size;
	int		i;

	if (!s)
		return (NULL);
	size = ft_strlen(s);
	new = malloc((size + 1) * sizeof(char));
	if (!new)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
